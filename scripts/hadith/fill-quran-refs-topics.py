def sort_refs(refs: list[str]) -> list[str]:
    return sorted(refs, key=lambda x: (int(x.split(':')[0]), int(x.split(':')[1])))


def process_collection(coll: str) -> tuple[int, int]:
    coll_dir = os.path.join(HADITH_DIR, coll)
    if not os.path.isdir(coll_dir):
        return 0, 0

    book_files = sorted(f for f in os.listdir(coll_dir) if f.endswith('.json'))
    total = 0
    updated = 0

    for book_file in book_files:
        path = os.path.join(coll_dir, book_file)
        try:
            data = json.load(open(path, encoding='utf-8'))
        except Exception:
            continue
        if not isinstance(data, list):
            continue

        changed = False
        for hadith in data:
            if not isinstance(hadith, dict):
                continue
            total += 1
            topics = hadith.get('topics', [])
            if not topics:
                continue

            existing_refs = set(hadith.get('quran_refs', []))
            new_refs = set()
            for topic in topics:
                if topic in TOPIC_QURAN_REFS:
                    new_refs.update(TOPIC_QURAN_REFS[topic])

            if new_refs:
                merged = existing_refs | new_refs
                if merged != existing_refs:
                    hadith['quran_refs'] = sort_refs(list(merged))
                    updated += 1
                    changed = True

        if changed:
            with open(path, 'w', encoding='utf-8') as f:
                json.dump(data, f, ensure_ascii=False, indent=2)
                f.write('\n')

    return total, updated


def main():
    total_hadiths = 0
    total_updated = 0

    for coll in COLLECTIONS:
        hadiths, updated = process_collection(coll)
        total_hadiths += hadiths
        total_updated += updated
        pct = 100 * updated / hadiths if hadiths else 0
        print(f'  {coll}: {hadiths} hadiths → {updated} updated ({pct:.1f}%)')

    print(f'\nTotal: {total_hadiths} hadiths, {total_updated} now have topic refs ({100*total_updated/total_hadiths:.1f}%)')

    # Final coverage check
    total_with_refs = 0
    for coll in COLLECTIONS:
        coll_dir = os.path.join(HADITH_DIR, coll)
        if not os.path.isdir(coll_dir):
            continue
        for f in os.listdir(coll_dir):
            if not f.endswith('.json'):
                continue
            data = json.load(open(os.path.join(coll_dir, f), encoding='utf-8'))
            if isinstance(data, list):
                for h in data:
                    if isinstance(h, dict) and h.get('quran_refs'):
                        total_with_refs += 1

    print(f'\nCumulative (Phase A + B): {total_with_refs}/{total_hadiths} hadiths have quran_refs ({100*total_with_refs/total_hadiths:.1f}%)')
    print(f'Remaining for LLM generation (Phase C-J): {total_hadiths - total_with_refs}')


if __name__ == '__main__':
    main()
