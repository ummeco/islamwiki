# Write all chapters
written = []
skipped = []
errors = []

for ch_num, trans in sorted(translations.items()):
    fpath = BOOK_DIR / f'{ch_num}.json'
    try:
        data = json.loads(fpath.read_text())

        if data.get('content_id', '').strip():
            skipped.append(ch_num)
            continue

        data['content_id'] = trans['content_id']
        if not data.get('title_id', '').strip():
            data['title_id'] = trans['title_id']

        fpath.write_text(json.dumps(data, ensure_ascii=False, indent=2) + '\n')
        written.append(ch_num)
    except Exception as e:
        errors.append(f'{ch_num}: {e}')

print(f'Written: {len(written)} chapters - {written}')
print(f'Skipped (already done): {len(skipped)} chapters - {skipped}')
if errors:
    print(f'Errors: {errors}')
